@RunWith(Parameterized::class)
class CmykToRgb(
    private val cyan: Float,
    private val magenta: Float,
    private val yellow: Float,
    private val key: Float,
    val red: Int,
    val green: Int,
    val blue: Int
) {


    @Test
    fun cmykToRGBTest(){
        val rgb = CodeCMYK(cyan,magenta,yellow,key).toRGB()

        println("$cyan/$magenta/$yellow/$key")
        Assert.assertThat(rgb,CoreMatchers.equalTo(CodeRGB(red, green, blue)))

    }

    @Test
    fun cmykToRGBInverseTest(){
        val cmyk = CodeCMYK(cyan,magenta,yellow,key)
        val cmykAlt = cmyk.toRGB()?.toCMYK()

        cmykAlt?.let {
            Assert.assertThat(cmykAlt,CoreMatchers.equalTo(cmyk))
        }
    }

    companion object{
        @Suppress("RemoveExplicitTypeArguments")  // Number 가 생략되면 에러남.
        @JvmStatic
        @Parameterized.Parameters
        fun data(): List<Array<Number>>{
            // total 2099 case
            return generateTestCase()
        }
    }

}

